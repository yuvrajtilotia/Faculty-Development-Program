        <h3 class="text-left"> Purpose of the Study </h3>
        <hr>
        <p> The purpose of this study was to determine if an FLC experience focused on collaborative student learning can have sustainable impacts 2–3 years after completion of the FLC. This study first identified what FLC structures were considered critical for initial implementation, given that faculty perspectives may have changed since the original implementation. Subsequently, it documented what collaborative learning techniques were retained, modified, or discontinued within faculty teaching practices in order to more fully characterize what might or might not have been sustained post-FLC. Finally, it illuminated characteristics and potential follow-up activities to support and sustain a community of practice focused on pedagogical change.

        Identifying ways faculty can sustain a long-term pedagogical change through an FLC experience is of high interest (Cox, 2004; Richlin & Cox, 2004). Although FLCs have been shown to be effective for initiating change (Furco & Moely, 2012), there is sparse evidence whether these changes are sustained or sustainable beyond the FLC participation. Determining what FLC structures encourage the long-term sustainability of any evidence-based teaching strategy within a faculty member’s teaching practice is of particular interest as universities work to implement professional development opportunities to encourage the widespread use of effective pedagogies grounded in how students learn (Borrego & Henderson, 2014). This study explored the institutional and community supports necessary to sustain pedagogical change.

        There were 14 total faculty participants from two FLC cohorts. Of the 14 participants, two did not fully engage with the educational goals and suggested pedagogical changes of the FLC during the initial FLC implementation and were therefore not invited to participate in this follow-up study since there had been no meaningful change to be sustained. The remaining 12 participants were interviewed, and their comments analyzed to answer research questions related to sustaining pedagogical change. </p>
