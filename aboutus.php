        <h3 class="text-left"> Introduction </h3>
        <hr>
        <p> How to encourage and sustain the use of evidence-based teaching practices among science, technology, engineering, and mathematics (STEM) fields is a critical topic. Professional development in teaching for university faculty is often scattered and not supported in a coordinated way by STEM Department administrators (Borrego, Froyd, & Hall, 2010; Fairweather, 2008). For several decades Professional Learning Communities (PLCs) have been a common practice in the K-12 teaching community, and they provide means for teachers to learn, share, and encourage each other to develop and test new pedagogies (Stoll, Bolam, McMahon, Wallace, & Thomas, 2006). More recently, practitioners of higher education have adopted a similar community professional development model to assist university teaching development and encourage pedagogical changes, referred to as a faculty learning community (FLC) (Cox, 2001). PLCs and FLCs are both examples of communities that share a common interest and practice, known as communities of practice (Lave & Wenger, 1991). This study takes a retrospective view of the enduring pedagogical changes initiated via faculty participation in an FLC and provides evidence that supports sustainable pedagogical practice acquired through the community of practice experience. 

        This research was conducted within the context of an engineering school at a large urban university. The administration of the engineering school, with support of its faculty, created a center dedicated to promoting educational excellence in the teaching of engineering. That center promotes professional development opportunities for faculty, with a mission to foster outstanding teaching and learning among engineering faculty. One specific goal is to advance the use of evidence-based teaching strategies. The faculty and the administration were aware of the challenge by Prince (2004) for engineering faculty to promote collaboration in their classes. Additionally, the accrediting agency for engineering programs (ABET, 2018) specifically links collaboration to the engineering curriculum via two of the 7 required student outcomes (ABET, 2018). The two outcomes directly related to a student’s ability to collaborate refer to “an ability to function effectively on a team” and “an ability to communicate effectively.” Also, employers desire graduates who can collaborate on teams but have reported that engineering students are not well prepared to do so (Jaschik, 2015). To encourage the faculty to use collaborative learning techniques, the dean of the school of engineering specifically redesigned one learning space for active and collaborative learning. As a result, he asked that the engineering education center provide professional development for faculty to make proper use of the new space. This led to the choice of collaborative student learning as the specific evidence-based teaching strategy as the focus for the FLC.

        The engineering education center partnered with the university’s professional development unit and a faculty member from the university’s education college to encourage and engage faculty in the use of collaborative student learning techniques throughout the engineering disciplines. Using the FLC framework, they offered two community of practice cohorts focused on the implementation of collaborative student learning techniques in engineering courses. One cohort was conducted during a full school year in 2014–2015, and another in 2015–2016.

        The challenge to encourage evidence-based change in teaching is not only how to educate faculty about the particular strategy, in this case, collaborative student learning techniques, but also how to support them as they attempt to implement paradigmatic pedagogical changes. We (Ralston, Tretter, & Brown, 2017) described the approach and the initial impact on the first cohort of faculty participants, including support structures that enabled faculty to implement collaborative learning techniques, as well as the benefits participants experienced from pedagogical shifts. However, in addition to supporting initial pedagogical change, an additional challenge is to understand if such change would be sustainable, long-term (beyond FLC duration) and what may have supported any long-term changes. </p>
